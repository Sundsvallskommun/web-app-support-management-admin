export const emailRegex :string = /[a-z0-9!#$%&'*+/=?^_`{|}~-]+(?:\.[a-z0-9!#$%&'*+/=?^_`{|}~-]+)*@(?:[a-z0-9](?:[a-z0-9-]*[a-z0-9])?\.)+[a-z0-9](?:[a-z0-9-]*[a-z0-9])?/;

export const isValidEmailOrEmpty = (email: string) :boolean => {
  return !email || email.length === 0 || RegExp(emailRegex).exec(email);
};
